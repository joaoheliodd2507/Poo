public class carro {
    String tipo;
    String cor;
    String marca;
    String modelo;
    String motor;
    String viraesquerda;
    String viradireita;
    String acendefarou;
    
   

    public String geTipo() {
        return tipo;
    }
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    public String getCor() {
        return cor;
    }
    public void setCor(String cor) {
        this.cor = cor;
    }
    public String getMarca() {
        return marca;
    }
    public void setMarca(String marca) {
        this.marca = marca;
    }
    public String getModelo() {
        return modelo;
    }
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    public String getMotor() {
        return motor;
    }
    public void setMotor(String motor) {
        this.motor = motor;
    }
    public String getViraEsquerda() {
        return viraesquerda;
    }
    public void setViraEsquerda(String viraesquerda) {
        this.viraesquerda = viraesquerda;
    }
    public String getViraDireita() {
        return viradireita;
    }
    public void setViraDireita(String viradireita) {
        this.viradireita = viradireita;
    }
    public String getAcendeFarou() {
        return acendefarou;
    }
    public void setAcendeFarou(String acendefarou) {
        this.acendefarou = acendefarou;
    }
}
